a = int(input("Enter an int: "))
b = int(input("Enter another int: "))

op = input("Enter operation (+, -, *, /): ")

if(op == '+'):
    print("Sum: ", a+b)
elif(op == '-'):
    print("Subtraction: ", a-b)
elif(op == '*'):
    print("Multiplication: ",a*b)

elif(op == '/'):
    print("Division: ",a/2)






